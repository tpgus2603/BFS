#include <iostream>
using namespace std;

int n, m;
int arr[8]; //기록

void func(int k) { // 현재 k+1자리수를 택함 
	if (k == m) { // m개를 모두 택했으면
		for (int i = 0; i < m; i++)
			cout << arr[i] << ' '; // arr에 기록해둔 수를 출력
		cout << '\n';
		return;
	}
	for (int i = 1; i <= n; i++) { // 1부터 n까지의 수에 대해
		{ // 아직 i가 사용되지 않았으면
			if (k != 0 && arr[k - 1] <= i)
			{
				arr[k] = i; // k번째 수를 i로 정함
				func(k + 1); // 다음 수를 정하러 한 단계 더 들어감
			}
			else if (k == 0)
			{
				arr[k] = i; // k번째 수를 i로 정함
				func(k + 1); // 다음 수를 정하러 한 단계 더 들어감
			}
		}
	}
}

int main(void) {
	ios::sync_with_stdio(0);
	cin.tie(0);
	cin >> n >> m;
	func(0);
}